public class RelationalOperators {//for this type of operators output is always boolean
	public static void main(String[] args) {
		int i=+97;
		int j=45;
		System.out.println("i less than j : "+(i<j));// <
		System.out.println("i greater than j : "+(i>j));// >
		System.out.println("i is less than or equal to j : "+(i<=j));// <=
		System.out.println("i is greater than or equal to j : "+(i>=j));// >=
		System.out.println("i is  equal to j : "+(i==j));// ==
		System.out.println("i is not equal to j: "+(i!=j));// !=


	}
}
