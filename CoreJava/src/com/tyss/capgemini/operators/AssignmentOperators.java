package com.tyss.capgemini.operators;

public class AssignmentOperators {
	public static void main(String[] args) {
           int i=10;// '=' is assignment operator
           
           i+=10;//20     '+=' compound assignment operator
           System.out.println(i);
           i*=10;//200
           System.out.println(i);
           i-=10;//190
           System.out.println(i);
           i/=10;//19
           System.out.println(i);
           i%=10;//9
           System.out.println(i);
           
           
	}
}
